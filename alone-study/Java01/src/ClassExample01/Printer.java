package ClassExample01;

public class Printer {
	
	// println() 메소드의 매개값으로는 int, boolean, double, Stirng 을 줄 수있음
	void println(int i) {
		System.out.println(i);
	}

	void println(String i) {
		System.out.println(i);
	}
	
	void println(double i) {
		System.out.println(i);
	}
	
	void println(boolean i) {
		System.out.println(i);
	}
	
}